package oct2nd;

public class OperatorsDemo3 {

	public static void main(String[] args) {
		
		int x=20;
		
		int y=10;
		
		System.out.println(x+y);
		
		System.out.println(x-y);
		
		System.out.println(x*y);
		
		//quotient
		System.out.println(x/y);
		
		//remainder after division
		System.out.println(x%y);
		
		//relational operators 
		
		System.out.println(x>y);
		
		System.out.println(x<y);
		
		System.out.println(x>=10);
		
		System.out.println(x<=10);
		
		System.out.println(x==y);
		
		System.out.println(x!=y);

		
		
		
	}

}
